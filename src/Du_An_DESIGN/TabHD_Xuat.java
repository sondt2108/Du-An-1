        double giamPT;
        double giamTien;

        if (txtGiamPt.getText().isEmpty()) {
            giamPT = 0;
        } else {
            giamPT = Double.parseDouble(txtGiamPt.getText());
        }

        if (txtGiamTien.getText().isEmpty()) {
            giamTien = 0;
        } else {
            giamTien = Double.parseDouble(txtGiamTien.getText());
        }

        double tongTienFinal = tongTien - ((tongTien * giamPT) / 100) - giamTien;
