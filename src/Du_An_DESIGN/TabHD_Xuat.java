    private void tblSanPhamHDXuatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamHDXuatMouseClicked
        if (evt.getClickCount() >= 2) {
            jdlThongTinSanPham jdl = new jdlThongTinSanPham((JFrame) SwingUtilities.getWindowAncestor(this), true);
            jdl.setVisible(true);
            if (Huy) {
                return;
            }
            // Lấy IDSanPham <=> Lấy sản phẩm theo ID

            String MaSP = tblSanPhamHDXuat.getValueAt(tblSanPhamHDXuat.getSelectedRow(), 0).toString();
            String TenSP = tblSanPhamHDXuat.getValueAt(tblSanPhamHDXuat.getSelectedRow(), 1).toString();
            double DonGia = Double.parseDouble(tblSanPhamHDXuat.getValueAt(tblSanPhamHDXuat.getSelectedRow(), 3).toString());
            double ThanhTien = DonGia * SoLuongMua;

            ThemSPVaotblCTHD(MaSP, TenSP, SoLuongMua, DonGia, ThanhTien, GhiChu);

            double tongTien = TinhTien();
            double tienDu;

            double giamPT;
            double giamTien;

            if (txtGiamPt.getText().isEmpty()) {
                giamPT = 0;
            } else {
                giamPT = Double.parseDouble(txtGiamPt.getText());
            }

            if (txtGiamTien.getText().isEmpty()) {
                giamTien = 0;
            } else {
                giamTien = Double.parseDouble(txtGiamTien.getText());
            }

            double tongTienFinal = tongTien - ((tongTien * giamPT) / 100) - giamTien;
            txtTongTien.setText(ChuyenDoi.LaySoString(tongTienFinal));

            if (txtTienKhachTra.getText().isEmpty()) {
                tienDu = 0;
            } else {
                double tienKhachTra = Double.parseDouble(txtTienKhachTra.getText());
                tienDu = tienKhachTra - tongTienFinal;
            }
            txtTienDu.setText(ChuyenDoi.LaySoString(tienDu));
        }
    }//GEN-LAST:event_tblSanPhamHDXuatMouseClicked

    private void ThemSPVaotblCTHD(String MaSP, String TenSP, double SoLuong, double DonGia, double ThanhTien, String GhiChu) {
        DefaultTableModel tblModel = (DefaultTableModel) tblCHHD.getModel();

        Object ojb[] = new Object[7];
        ojb[0] = tblModel.getRowCount() + 1;
        ojb[1] = MaSP;
        ojb[2] = TenSP;
        ojb[3] = SoLuong;
        ojb[4] = ChuyenDoi.DinhDangTien(DonGia);
        ojb[5] = ChuyenDoi.DinhDangTien(ThanhTien);
        ojb[6] = GhiChu;

        tblModel.addRow(ojb);
    }

    private double TinhTien() {
        double tongTien = 0;
        for (int i = 0; i < tblCHHD.getRowCount(); i++) {
            tongTien += ChuyenDoi.ChuyenTien(tblCHHD.getValueAt(i, 5).toString());
        }

        return tongTien;
    }

