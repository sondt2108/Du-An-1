    private void SbtnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SbtnSuaActionPerformed
        String MaNV = txtMaNhanVien.getText();
        String TenNV = txtTenNhanVien.getText();
        boolean GioiTinh = rdbNam.isSelected();
        Date NgaySinh = txtNgaySinh.getDate();
        String ChucVu;
            Mycombobox myCbb = (Mycombobox) cbbMaChucVu.getSelectedItem();
            ChucVu = myCbb.MaString();
        //double Luong = ChuyenDoi.LaySoDouble(txtLuong.getText());
        String SDT = txtSĐT.getText();
        String DiaChi = txtDiaChi.getText();
        String SoCMND = txtSoCMND.getText();
        Date NgayVaoLam = txtNgayVaoLam.getDate();
        String TenDangNhap = txtTenDangNhap.getText();
        String MatKhau = txtMatKhau.getText();
        String HinhAnh = txtHinhAnh.getText();
        String Email = txtEmail.getText();
        if (this.txtTenNhanVien.getText().length() == 0) {
            ThongBao.ThongBaoSQL("Thông Báo", "Họ Và Tên Không Được Bỏ Trống");
        } else if (this.txtDiaChi.getText().length() == 0) {
            ThongBao.ThongBaoSQL("Thông Báo", "Địa Chỉ Không Được Bỏ Trống");
        } else if (this.txtSoCMND.getText().length() == 0) {
            ThongBao.ThongBaoSQL("Thông Báo", "Số CMND Không Được Bỏ Trống");
        } else if (this.txtEmail.getText().length() == 0) {
            ThongBao.ThongBaoSQL("Thông Báo", " Email Không Được Bỏ Trống");
        } else if (this.txtSĐT.getText().length() < 10) {
            ThongBao.ThongBaoSQL("Thông Báo", "Số Điện Thoại Được Bỏ Trống");
        } else if (this.txtTenDangNhap.getText().length() == 0) {
            ThongBao.ThongBaoSQL("Thông Báo", "Tên Đăng Nhập Được Bỏ Trống");
        } else if (this.txtMatKhau.getText().length() == 0) {
            ThongBao.ThongBaoSQL("Thông Báo", "Mật Khẩu Được Bỏ Trống");
        } else {
            NhanVien nv = new NhanVien(MaNV, TenNV, GioiTinh, NgaySinh, ChucVu, DiaChi, SDT, SoCMND, NgayVaoLam, TenDangNhap, MatKhau, Email, HinhAnh);

            BLLNhanVien.SuaNhanVien(nv);
             ThongBao.ThongBaoDangNhap("Thông Báo", " Đã Sửa Thành Công");
            BLLNhanVien.LoadNhanVien(tblDanhSach);
        }
    }//GEN-LAST:event_SbtnSuaActionPerformed

    private void SbtnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SbtnXoaActionPerformed
        if (this.txtMaNhanVien.getText().length() == 0) {
            ThongBao.ThongBaoSQL("Thông Báo", " Bạn Chưa Chọn Dòng Để Xoá !!");

        } else {

            int DSViTri[] = tblDanhSach.getSelectedRows();
            List<String> lstMaSP = new ArrayList<>();

            for (int i : DSViTri) {
                String MaNV = tblDanhSach.getValueAt(i, 1).toString();
                lstMaSP.add(MaNV);
            }

            BLLNhanVien.Delete(lstMaSP);
             ThongBao.ThongBaoDangNhap("Thông Báo", " Xóa Thành Công");

            BLLNhanVien.LoadNhanVien(tblDanhSach);
        }
    }//GEN-LAST:event_SbtnXoaActionPerformed

