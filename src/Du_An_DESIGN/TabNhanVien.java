    private void SbtnChonHinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SbtnChonHinhActionPerformed
        // TODO add your handling code here:
         JFileChooser fileChooser = new JFileChooser("image");
        FileFilter filter = new FileNameExtensionFilter("Image file", "gjf", "jpg", "png");

        fileChooser.setFileFilter(filter);
        fileChooser.setMultiSelectionEnabled(false);

        int action = fileChooser.showOpenDialog(this);
        if (action == JFileChooser.APPROVE_OPTION) {
            while (true) {
                if (!fileChooser.getSelectedFile().getName().matches("([^\\s]+(\\.(?i)(jpg|png|gif|bmp))$)")) {
                    JOptionPane.showMessageDialog(null, "Định dạng file hình đuôi phải là GIF, JPG, PNG !");
                    return;
                } else {
                    break;
                }
            }
            String imgFile = fileChooser.getSelectedFile().getName();
            txtHinhAnh.setText(imgFile);
            img.setImageLabel(lblHinhAnh, "image//" + imgFile);
        }

    }//GEN-LAST:event_SbtnChonHinhActionPerformed

    private void SbtnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SbtnThemActionPerformed
        txtMaNhanVien.setText(BLLNhanVien.TaoMaNV());
        String MaNV = txtMaNhanVien.getText();
        String TenNV = txtTenNhanVien.getText();
        boolean GioiTinh = rdbNam.isSelected();
        Date NgaySinh = txtNgaySinh.getDate();
        String ChucVu;
            Mycombobox myCbb = (Mycombobox) cbbMaChucVu.getSelectedItem();
            ChucVu = myCbb.MaString();
        //double Luong = ChuyenDoi.LaySoDouble(txtLuong.getText());
        String SDT = txtSĐT.getText();
        String DiaChi = txtDiaChi.getText();
        String SoCMND = txtSoCMND.getText();
        Date NgayVaoLam = txtNgayVaoLam.getDate();
        String TenDangNhap = txtTenDangNhap.getText();
        String MatKhau = txtMatKhau.getText();
        String HinhAnh = txtHinhAnh.getText();
        String Email = txtEmail.getText();
        
        if(this.txtTenNhanVien.getText().length() == 0 || this.txtDiaChi.getText().length() == 0 ||
           this.txtSoCMND.getText().length() == 0 || this.txtEmail.getText().length() == 0 ||
           this.txtSĐT.getText().length() == 0 || this.txtTenDangNhap.getText().length() == 0 ||
           this.txtMatKhau.getText().length() == 0
                ) {
        if (this.txtTenNhanVien.getText().length() == 0) {
            
            lbltest.setVisible(true);
            lbltest.setText("Họ Và Tên Không Được Bỏ Trống");
        }  
        if (this.txtDiaChi.getText().length() == 0) {
            lblDC.setVisible(true);
            lblDC.setText("Địa Chỉ Không Được Bỏ Trống");
        }  
        if (this.txtSoCMND.getText().length() == 0) {
            lblCM.setVisible(true);
            lblCM.setText("Số CMND Không Được Bỏ Trống");
        }  
        if (this.txtEmail.getText().length() == 0) {
            lblGmail.setVisible(true);
            lblGmail.setText("Email Không Được Bỏ Trống");
        } 
        if (this.txtSĐT.getText().length() == 0) {
            lblSdt.setVisible(true);
            lblSdt.setText("Số Điện Thoại Không Được Bỏ Trống");
        }
        if (this.txtTenDangNhap.getText().length() == 0) {
            lblTenDN.setVisible(true);
            lblTenDN.setText("Tên Đăng Nhập Không Được Bỏ Trống");
        }
        if (this.txtMatKhau.getText().length() == 0) {
            lblPass.setVisible(true);
            lblPass.setText("Mật Khẩu Không Được Bỏ Trống");
        }
        }else {
            
            NhanVien nv = new NhanVien(MaNV, TenNV, GioiTinh, NgaySinh, ChucVu, DiaChi, SDT, SoCMND, NgayVaoLam, TenDangNhap, MatKhau, Email, HinhAnh);

            BLLNhanVien.ThemNhanVien(nv);
             ThongBao.ThongBaoDangNhap("Thông Báo", " Thêm Thành Công");
            BLLNhanVien.LoadNhanVien(tblDanhSach);
        }
    }//GEN-LAST:event_SbtnThemActionPerformed

    private void CbbChonCV(String TenLoaiSP) {
        DefaultComboBoxModel cbbModel = (DefaultComboBoxModel) cbbMaChucVu.getModel();

        for (int i = 0; i < cbbMaChucVu.getItemCount(); i++) {
            Mycombobox myCBB = (Mycombobox) cbbModel.getElementAt(i);

            if (myCBB.MaString().toString().equals(TenLoaiSP)) {
                cbbMaChucVu.setSelectedIndex(i);
                break;
            }
        }
    }
    
    private void tblDanhSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDanhSachMouseClicked
       int dongDuocChon = tblDanhSach.getSelectedRow();
        String MaNV = tblDanhSach.getValueAt(dongDuocChon, 1).toString();
        NhanVien nv = BLLNhanVien.LayNhanVienTheoMa(MaNV);
        txtMaNhanVien.setText(nv.getMaNV() + "");
        String image = (String) tblDanhSach.getValueAt(dongDuocChon, 13);

        //   cbbMaChucVu.setSelectedItem(nv.getMaChucVu());
        //BLLCombobox.setComboSelectValue(cbbMaChucVu, nv.getChucVu());
        txtTenNhanVien.setText(nv.getTenNV());
        if (nv.isGioiTinh()) {
            rdbNam.setSelected(true);
        } else {
            rdbNu.setSelected(true);
        }

        txtNgaySinh.setDate(nv.getNgaySinh());
        txtDiaChi.setText(nv.getDiaChi());
        txtSĐT.setText(nv.getSDT());
        txtSoCMND.setText(nv.getCMND());
        txtEmail.setText(nv.getEmail());
        txtTenDangNhap.setText(nv.getTenDangNhap());
        txtMatKhau.setText(nv.getMatKhau());
        txtNgayVaoLam.setDate(nv.getNgayVaoLam());
        txtHinhAnh.setText(image);
        CbbChonCV(nv.getChucVu());
        img.setImageLabel(lblHinhAnh, "image//" + image);

        // Chuyển Về Tab Đầu Tiên
        lblHinhAnh.setText("");
    }//GEN-LAST:event_tblDanhSachMouseClicked

