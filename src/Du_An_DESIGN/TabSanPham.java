        SanPham sp = BLLSanPham.LaySanPhamTheoMa(MaNV);
        txtMaSanPham.setText(sp.getMaSanPham() + "");
        String image = (String) tblDanhSach.getValueAt(dongDuocChon, 11);

        txtTenSanPham.setText(sp.getTenSanPham());
