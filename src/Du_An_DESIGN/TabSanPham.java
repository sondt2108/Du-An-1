        CbbChonLoaiSP(sp.getMaLoaiSP());
        //cbbChucVu.setSelectedItem(nv.getChucvu());
        //cbbHangSX.setSelectedItem(sp.getMaHangSx());
        CbbChonHangSX(sp.getMaHangSX());
